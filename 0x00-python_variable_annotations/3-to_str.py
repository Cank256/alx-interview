#!/usr/bin/env python3
"""A module for converting a float to a string.

This module provides a simple function to convert a float to a string.
"""


def to_str(n: float) -> str:
    """Convert a float to a string.

    Parameters:
    n (float): The number to convert.

    Returns:
    str: The string representation of n.
    """
    return str(n)
