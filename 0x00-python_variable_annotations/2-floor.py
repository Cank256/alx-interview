#!/usr/bin/env python3
"""A module for flooring a float number.

This module provides a simple function to floor a float number.
"""

from math import floor


def floor(n: float) -> int:
    """Return the floor of a float number.

    Parameters:
    n (float): The number to floor.

    Returns:
    int: The floor of n.
    """
    return floor(n)
